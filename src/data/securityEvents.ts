function generateProcessTree(cmd: string): SecurityEvent['processTree'] {
  return {
    pid: 1,
    name: 'systemd',
    command: '/sbin/init',
    user: 'root',
    children: [
      {
        pid: Math.floor(Math.random() * 5000) + 100,
        name: 'containerd-shim',
        command: 'containerd-shim -namespace moby',
        user: 'root',
        children: [
          {
            pid: Math.floor(Math.random() * 10000) + 5000,
            name: cmd.split(' ')[0].split('/').pop() || 'process',
            command: cmd,
            user: randomFrom(users),
            children: [],
          },
        ],
      },
    ],
  };
}

function generateTimeline(title: string): SecurityEvent['timeline'] {
  const baseTime = new Date(Date.now() - Math.random() * 3600000);
  return [
    { timestamp: new Date(baseTime.getTime() - 120000), action: 'Network connection established', detail: `Outbound connection from container to ${randomFrom(extIps)}`, severity: 'info' as Severity },
    { timestamp: new Date(baseTime.getTime() - 60000), action: 'Suspicious process spawned', detail: `Unexpected process created in container namespace`, severity: 'medium' as Severity },
    { timestamp: new Date(baseTime.getTime() - 30000), action: 'Rule triggered', detail: `Detection rule matched: ${title}`, severity: 'high' as Severity },
    { timestamp: baseTime, action: 'Alert generated', detail: `Security alert created and forwarded to monitoring`, severity: 'critical' as Severity },
    { timestamp: new Date(baseTime.getTime() + 5000), action: 'Event correlated', detail: `Correlated with ${Math.floor(Math.random() * 5) + 1} related events`, severity: 'info' as Severity },
  ];
}

function generateRawLogs(template: EventTemplate): string[] {
  const ts = new Date().toISOString();
  return [
    `[${ts}] INFO  deepshield.engine - Processing event from ${template.tool}`,
    `[${ts}] DEBUG deepshield.rules  - Evaluating rule ${template.ruleId}`,
    `[${ts}] WARN  deepshield.detect - ${template.title} detected`,
    `[${ts}] INFO  deepshield.meta   - Source: ${randomFrom(ips)} -> Dest: ${randomFrom(extIps)}`,
    `[${ts}] DEBUG deepshield.proc   - Command: ${template.command}`,
    `[${ts}] INFO  deepshield.mitre  - Mapped to ${template.mitreTId}: ${template.mitreT}`,
    `[${ts}] INFO  deepshield.alert  - Alert priority: ${template.priority} severity: ${template.severity}`,
    `[${ts}] DEBUG deepshield.corr   - Correlation engine: checking for related events`,
    `[${ts}] INFO  deepshield.store  - Event stored in index: deepshield-events-${new Date().toISOString().slice(0, 10)}`,
    `[${ts}] INFO  deepshield.notify - Notification dispatched to dashboard`,
  ];
}

export function generateEvent(templateIndex?: number): SecurityEvent {
  const idx = templateIndex ?? Math.floor(Math.random() * EVENT_TEMPLATES.length);
  const template = EVENT_TEMPLATES[idx];
  const host = randomFrom(hosts);
  const containerIdx = Math.floor(Math.random() * containers.length);
  const srcIp = randomFrom(ips);
  const dstIp = randomFrom(extIps);

  return {
    id: uid(),
    eventId: `EVT-${Date.now().toString(36).toUpperCase()}-${Math.random().toString(36).slice(2, 6).toUpperCase()}`,
    title: template.title,
    description: template.description,
    priority: template.priority,
    severity: template.severity,
    tool: template.tool,
    timestamp: randomDate(2),
    status: randomFrom(['active', 'active', 'active', 'investigating'] as ThreatStatus[]),
    host,
    container: containers[containerIdx],
    containerId: containerIds[containerIdx],
    image: randomFrom(images),
    namespace: randomFrom(namespaces),
    pod: randomFrom(pods),
    count: 1,
    ruleId: template.ruleId,
    metadata: {
      sourceIP: srcIp,
      destinationIP: dstIp,
      port: randomFrom([22, 80, 443, 4444, 8080, 8443, 9090, 3306, 5432, 6379]),
      protocol: randomFrom(['TCP', 'UDP', 'HTTP', 'HTTPS', 'DNS', 'SSH']),
      user: randomFrom(users),
      pid: Math.floor(Math.random() * 32768) + 1000,
      command: template.command,
      parentProcess: randomFrom(['containerd-shim', 'dockerd', 'kubelet', 'systemd', 'bash']),
      workingDirectory: randomFrom(['/app', '/tmp', '/var/www/html', '/home/user', '/opt/service']),
      fileHash: `sha256:${Array.from({ length: 64 }, () => '0123456789abcdef'[Math.floor(Math.random() * 16)]).join('')}`,
      networkBytes: Math.floor(Math.random() * 1000000),
    },
    mitreAttack: {
      tactic: template.mitreTactic,
      tacticId: template.mitreTacticId,
      technique: template.mitreT,
      techniqueId: template.mitreTId,
      description: `${template.mitreT} - ${template.description.slice(0, 100)}`,
    },
    processTree: generateProcessTree(template.command),
    timeline: generateTimeline(template.title),
    rawLogs: generateRawLogs(template),
    suggestedFix: template.suggestedFix,
    riskScore: template.severity === 'critical' ? 90 + Math.floor(Math.random() * 10) : template.severity === 'high' ? 70 + Math.floor(Math.random() * 20) : template.severity === 'medium' ? 40 + Math.floor(Math.random() * 30) : 10 + Math.floor(Math.random() * 30),
  };
}

